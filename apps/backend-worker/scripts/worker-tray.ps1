function Append-AgentEventsToLog {
  if (-not $script:logTextBox -or $script:logTextBox.IsDisposed) { return }
  Refresh-AgentBaseUrl
  try {
    $res = Invoke-RestMethod -Uri "$($script:agentBaseUrl)/events?since=$lastLogSeq" -Method Get -TimeoutSec 3
    if (-not $res.ok) { return }
    $nextSeq = $script:lastLogSeq
    if ($null -ne $res.latestSeq) {
      $trySeq = 0
      if ([int]::TryParse("$($res.latestSeq)", [ref]$trySeq)) {
        $nextSeq = $trySeq
      }
    }
    $script:lastLogSeq = $nextSeq
    if ($res.events -and $res.events.Count -gt 0) {
      $lines = @()
      foreach ($item in $res.events) {
        if ($item.line) { $lines += [string]$item.line }
      }
      if ($lines.Count -gt 0) {
        if ($script:logTextBox.TextLength -gt 0) { $script:logTextBox.AppendText("`r`n") }
        $script:logTextBox.AppendText(($lines -join "`r`n"))
        if ($script:logTextBox.TextLength -gt 30000) {
          $script:logTextBox.Text = $script:logTextBox.Text.Substring($script:logTextBox.TextLength - 15000)
        }
        $script:logTextBox.SelectionStart = $script:logTextBox.TextLength
        $script:logTextBox.ScrollToCaret()
      }
    }
  } catch {
    # keep silent to avoid lag popup
  }
}

