$settingsItem.Enabled = $false
$startupItem.add_CheckedChanged({
  try {
    Set-StartupEnabled ([bool]$startupItem.Checked)
    if ($startupItem.Checked) {
      $notifyIcon.BalloonTipTitle = "CogniMail Worker"
      $notifyIcon.BalloonTipText = "Da bat tu chay cung Windows."
      $notifyIcon.ShowBalloonTip(1200)
    } else {
      $notifyIcon.BalloonTipTitle = "CogniMail Worker"
      $notifyIcon.BalloonTipText = "Da tat tu chay cung Windows."
      $notifyIcon.ShowBalloonTip(1200)
    }
  } catch {
    $startupItem.Checked = Is-StartupEnabled
    [System.Windows.Forms.MessageBox]::Show("Khong doi duoc che do startup: $($_.Exception.Message)", "CogniMail Worker", "OK", "Error") | Out-Null
  }
})
$healthItem.add_Click({ Invoke-AgentHealth })
$logsItem.add_Click({ Open-LogWindow })
$syncItem.add_Click({ Invoke-AgentSyncNow })
$openItem.add_Click({ Start-Process explorer.exe $root })
$versionItem.Enabled = $false
$exitItem.add_Click({
  Stop-Worker
  $notifyIcon.Visible = $false
  [System.Windows.Forms.Application]::Exit()
})

