function Read-EnvMap {
  $map = [ordered]@{}
  if (!(Test-Path $envPath)) { return $map }
  $lines = Get-Content -Path $envPath
  foreach ($line in $lines) {
    if ([string]::IsNullOrWhiteSpace($line)) { continue }
    if ($line.TrimStart().StartsWith("#")) { continue }
    $idx = $line.IndexOf("=")
    if ($idx -lt 1) { continue }
    $key = $line.Substring(0, $idx).Trim()
    $value = $line.Substring($idx + 1)
    $map[$key] = $value
  }
  return $map
}

function Write-EnvMap([hashtable]$map) {
  $preferredOrder = @(
    "FIREBASE_WEB_API_KEY",
    "FIREBASE_AUTH_DOMAIN",
    "FIREBASE_PROJECT_ID",
    "FIREBASE_STORAGE_BUCKET",
    "FIREBASE_MESSAGING_SENDER_ID",
    "FIREBASE_APP_ID",
    "USER_EMAIL",
    "WORKER_INTERVAL_MINUTES",
    "WORKER_LIMIT",
    "WORKER_AGENT_PORT",
    "OAUTH_CALLBACK_PORT"
  )

  $lines = New-Object System.Collections.Generic.List[string]
  $lines.Add("# Auto-generated by setup-worker.ps1")
  foreach ($key in $preferredOrder) {
    if ($map.Contains($key)) {
      $lines.Add("$key=$($map[$key])")
      $map.Remove($key) | Out-Null
    }
  }
  foreach ($key in $map.Keys) {
    $lines.Add("$key=$($map[$key])")
  }
  Set-Content -Path $envPath -Value ($lines -join "`r`n") -Encoding UTF8
}

function To-BoundedInt([string]$value, [int]$defaultValue, [int]$minValue, [int]$maxValue) {
  $parsed = 0
  if (![int]::TryParse($value, [ref]$parsed)) {
    return $defaultValue
  }
  if ($parsed -lt $minValue) { return $minValue }
  if ($parsed -gt $maxValue) { return $maxValue }
  return $parsed
}

function Get-StartupCommand {
  $exePath = [System.Windows.Forms.Application]::ExecutablePath
  if ($exePath -and (Test-Path $exePath) -and $exePath.ToLower().EndsWith(".exe")) {
    return "`"$exePath`""
  }
  if ($PSCommandPath -and (Test-Path $PSCommandPath)) {
    return "powershell.exe -ExecutionPolicy Bypass -WindowStyle Hidden -File `"$PSCommandPath`""
  }
  return $null
}

function Is-StartupEnabled {
  try {
    $item = Get-ItemProperty -Path $startupRegPath -Name $startupValueName -ErrorAction Stop
    $value = [string]$item.$startupValueName
    return -not [string]::IsNullOrWhiteSpace($value)
  } catch {
    return $false
  }
}

function Set-StartupEnabled([bool]$enabled) {
  if (!(Test-Path $startupRegPath)) {
    New-Item -Path $startupRegPath -Force | Out-Null
  }
  if ($enabled) {
    $command = Get-StartupCommand
    if ([string]::IsNullOrWhiteSpace($command)) {
      throw "Khong tao duoc lenh startup."
    }
    Set-ItemProperty -Path $startupRegPath -Name $startupValueName -Value $command -Type String
    return
  }
  Remove-ItemProperty -Path $startupRegPath -Name $startupValueName -ErrorAction SilentlyContinue
}

