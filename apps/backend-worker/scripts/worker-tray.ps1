function Find-WorkerPids {
  try {
    $needle = [Regex]::Escape($workerEntry)
    $processes = Get-CimInstance Win32_Process -Filter "Name='node.exe'" -ErrorAction SilentlyContinue
    if (-not $processes) { return @() }
    return @(
      $processes |
        Where-Object { $_.CommandLine -and ($_.CommandLine -match "worker-user-direct\.js" -or $_.CommandLine -match $needle) } |
        ForEach-Object { [int]$_.ProcessId }
    )
  } catch {
    return @()
  }
}

function Start-Worker {
  Refresh-AgentBaseUrl
  try {
    $existing = Invoke-RestMethod -Uri "$($script:agentBaseUrl)/ping" -Method Get -TimeoutSec 2
    if ($existing -and $existing.ok) { return }
  } catch {}
  if ($script:workerProcess -and -not $script:workerProcess.HasExited) { return }
  foreach ($workerPid in (Find-WorkerPids)) {
    try { Stop-Process -Id $workerPid -Force -ErrorAction SilentlyContinue } catch {}
  }
  if (!(Test-WorkerEnvReady)) {
    $base = [ordered]@{
      FIREBASE_WEB_API_KEY = "AIzaSyCo1W3Fx8hvUqYB5joqjaAKUMnDAz0fqLM"
      FIREBASE_AUTH_DOMAIN = "cognimail-fa0c0.firebaseapp.com"
      FIREBASE_PROJECT_ID = "cognimail-fa0c0"
      FIREBASE_STORAGE_BUCKET = "cognimail-fa0c0.firebasestorage.app"
      FIREBASE_MESSAGING_SENDER_ID = "906136722896"
      FIREBASE_APP_ID = "1:906136722896:web:3dc2809bf51d11ba302dd5"
      WORKER_INTERVAL_MINUTES = "15"
      WORKER_LIMIT = "5"
      WORKER_AGENT_PORT = "41731"
      OAUTH_CALLBACK_PORT = "1455"
    }
    Write-EnvMap $base
  }

  if (!(Test-Path $workerEntry)) {
    [System.Windows.Forms.MessageBox]::Show("Thieu file dist\\worker-user-direct.js. Hay giai nen toan bo file zip worker vao 1 thu muc rieng, sau do chay lai WorkerTray.exe.", "CogniMail Worker", "OK", "Error") | Out-Null
    return
  }

  $nodeCmd = if (Test-Path $localNodeExe) { $localNodeExe } else { "node" }

  if ($nodeCmd -eq "node") {
    $nodeAvailable = Get-Command node -ErrorAction SilentlyContinue
    if (-not $nodeAvailable) {
      [System.Windows.Forms.MessageBox]::Show("Khong tim thay node runtime. Hay dung ban worker dong goi chuan (co node.exe).", "CogniMail Worker", "OK", "Error") | Out-Null
      return
    }
  }

  $oldEnv = $env:WORKER_ENV_PATH
  $env:WORKER_ENV_PATH = $envPath
  try {
    $script:workerProcess = Start-Process -FilePath $nodeCmd -ArgumentList "`"$workerEntry`"" -WorkingDirectory $root -WindowStyle Hidden -PassThru
  } finally {
    if ([string]::IsNullOrEmpty($oldEnv)) {
      Remove-Item Env:WORKER_ENV_PATH -ErrorAction SilentlyContinue
    } else {
      $env:WORKER_ENV_PATH = $oldEnv
    }
  }
}

function Stop-Worker {
  if ($script:workerProcess -and -not $script:workerProcess.HasExited) {
    try { Stop-Process -Id $script:workerProcess.Id -Force } catch {}
  }
  foreach ($workerPid in (Find-WorkerPids)) {
    try { Stop-Process -Id $workerPid -Force -ErrorAction SilentlyContinue } catch {}
  }
  $script:workerProcess = $null
}

