function Refresh-TrayStatus {
  try {
    $ping = Get-AgentPing
    if (-not $ping.ok) {
      Set-TrayState "off" ""
      return
    }
    $res = Get-AgentHealth
    if ($res.syncing -or $res.stage -eq "fetching-imap" -or $res.stage -eq "saving-firestore") {
      Set-TrayState "fetching" ""
      return
    }
    if ($res.lastError) {
      Set-TrayState "error" "$($res.lastError)"
      return
    }
    Set-TrayState "on" ""
  } catch {
    $workerCount = (Find-WorkerPids).Count
    if ($workerCount -eq 0) {
      Set-TrayState "starting" ""
      Start-Worker
      return
    }
    if ($script:lastTrayState -eq "fetching") {
      Set-TrayState "fetching" "Worker dang ban, trang thai tra ve cham."
      return
    }
    Set-TrayState "on" "Worker dang chay, nhung status timeout tam thoi."
  }
}

